SELECT 
CITY,COUNT(DISTINCT(FICHENO)) AS MUSTERISAYISI,COUNT(*) AS BARKODSAYISI
FROM SALES WHERE MONTHNAME_='01 OCAK'
GROUP BY CITY
ORDER BY CITY

SELECT 
CITY,COUNT(DISTINCT(CUSTOMERNAME)) AS TEKILMUSTERI, COUNT(DISTINCT(FICHENO)) AS MUSTERISAYISI,COUNT(*) AS BARKODSAYISI
FROM SALES WHERE MONTHNAME_='01 OCAK'
GROUP BY CITY
ORDER BY CITY

